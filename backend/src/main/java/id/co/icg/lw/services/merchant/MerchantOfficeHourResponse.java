package id.co.icg.lw.services.merchant;

import id.co.icg.lw.domain.merchant.MerchantOfficeHour;

public class MerchantOfficeHourResponse {
    private int day;
    private String open;
    private String close;

    public MerchantOfficeHourResponse() {}

    public MerchantOfficeHourResponse(MerchantOfficeHour merchantOfficeHour) {
        this.day = merchantOfficeHour.getDay();
        this.open = merchantOfficeHour.getStartTime();
        this.close = merchantOfficeHour.getEndTime();
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public String getOpen() {
        return open;
    }

    public void setOpen(String open) {
        this.open = open;
    }

    public String getClose() {
        return close;
    }

    public void setClose(String close) {
        this.close = close;
    }
}
